// Needed to load environment variables
import "dotenv/config";
import { PassThrough } from "node:stream";

import type { AppLoadContext, EntryContext } from "@remix-run/node";
import { createReadableStreamFromReadable } from "@remix-run/node";
import { RemixServer } from "@remix-run/react";
import { isbot } from "isbot";
import { renderToPipeableStream } from "react-dom/server";
import { connectDB } from "~/core/db/db.server";
import { logger } from "~/core/utils/logger.server";
import { systemConfigService } from "./services/systemConfig.service.server";
import { initializeCronJobs } from "~/utils/cron.job.server";
import path from "node:path";
import fs from "node:fs";

const ABORT_DELAY = 5_000;

// Create an initialization promise that ensures the database is connected,
// system configuration is initialized, and cron jobs are set up before handling requests.
const initPromise = (async () => {
  try {
    // Connect to the database
    await connectDB();
    logger.info("Connected to the database.");

    // Initialize system configuration
    await systemConfigService.initConfig();
    logger.info("System configuration initialized.");

    // Initialize cron jobs
    await initializeCronJobs();
    logger.info("Cron jobs initialized.");


    // Ensure to create /public/uploads directory in the root of the project
    // to store uploaded files
    fs.mkdirSync(path.join(process.cwd(), "public", "uploads"), {
      recursive: true,
    });
    
  } catch (error: any) {
    logger.error(`Failed to initialize application: ${error.message}`);
    process.exit(1); // Exit the process if initialization fails
  }
})();

export default async function handleRequest(
  request: Request,
  responseStatusCode: number,
  responseHeaders: Headers,
  remixContext: EntryContext,
  // This is ignored so we can keep it in the template for visibility.
  // Feel free to delete this parameter in your app if you're not using it!
  // eslint-disable-next-line @typescript-eslint/no-unused-vars
  loadContext: AppLoadContext
) {
  // Wait for the initialization to complete before handling requests
  await initPromise;

  return isbot(request.headers.get("user-agent") || "")
    ? handleBotRequest(
        request,
        responseStatusCode,
        responseHeaders,
        remixContext
      )
    : handleBrowserRequest(
        request,
        responseStatusCode,
        responseHeaders,
        remixContext
      );
}

function handleBotRequest(
  request: Request,
  responseStatusCode: number,
  responseHeaders: Headers,
  remixContext: EntryContext
) {
  return new Promise((resolve, reject) => {
    let shellRendered = false;

    const { pipe, abort } = renderToPipeableStream(
      <RemixServer
        context={remixContext}
        url={request.url}
        abortDelay={ABORT_DELAY}
      />,
      {
        onAllReady() {
          shellRendered = true;
          const body = new PassThrough();
          const stream = createReadableStreamFromReadable(body);

          responseHeaders.set("Content-Type", "text/html");

          resolve(
            new Response(stream, {
              headers: responseHeaders,
              status: responseStatusCode,
            })
          );

          pipe(body);
        },
        onShellError(error: unknown) {
          reject(error);
        },
        onError(error: unknown) {
          responseStatusCode = 500;
          // Log streaming rendering errors from inside the shell.
          // Don't log errors encountered during initial shell rendering since they'll
          // reject and get logged in handleDocumentRequest.
          if (shellRendered) {
            console.error(error);
          }
        },
      }
    );

    setTimeout(abort, ABORT_DELAY);
  });
}

